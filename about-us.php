            },
        });

        $(".accordion-link").click(function() {
            const parentItem = $(this).closest('.accordion-item');
            const isOpen = parentItem.hasClass('active');
            $('.accordion-item').removeClass('active');
            if (!isOpen) {
                parentItem.addClass('active');
            }
        });
